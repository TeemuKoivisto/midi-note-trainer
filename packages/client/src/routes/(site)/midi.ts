const midiToNoteMap = new Map(Object.entries(noteToMidiMap).map(([n, v]) => [v, n]))

export function midiToNote(val: number) {
  const n = midiToNoteMap.get(val)
  if (n === undefined) {
    console.error('Unknown note! ', val)
    return 'C0'
  }
  return n
}

export function toNote(val: number) {
  const noteSeq = (val - 12) % 12
  const isFlatOrSharp =
    noteSeq === 1 || noteSeq === 3 || noteSeq === 6 || noteSeq === 8 || noteSeq === 10
}
